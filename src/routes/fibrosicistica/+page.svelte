<h2 id="la-fibrosi-cistica">
	Cos'è la Fibrosi Cistica
</h2>
<div>
	<p class="intro">
		La <em>fibrosi cistica</em> è la <em>malattia genetica grave più diffusa</em>. 
		<br/>
		E’ una <em>patologia multiorgano</em>, che colpisce soprattutto <em>l’apparato respiratorio</em> e
		quello <em>digerente</em>. E’ dovuta ad un gene alterato, cioè mutato, chiamato <em>gene CFTR</em>
		(Cystic Fibrosis Transmembrane Regulator), che determina la produzione di <em>muco eccessivamente
		denso</em>. Questo muco chiude i bronchi e porta a infezioni respiratorie ripetute, ostruisce
		il pancreas e impedisce che gli enzimi pancreatici raggiungano l’intestino, di conseguenza
		i cibi non possono essere digeriti e assimilati.
		<br/>
		Seppure il grado di coinvolgimento differisca anche notevolmente da persona a persona,
		la persistenza dell’infezione e dell’infiammazione polmonare, che causa il deterioramento
		progressivo del tessuto polmonare, è la maggior causa di morbilità nei pazienti FC.
		Le manifestazioni tipiche della malattia sono:
	</p>
	<video class="spot">
		<source src="https://youtu.be/kWGrxZOAI-U">
		<track default kind="captions">
	</video>
</div>
<ul>
	<li>
		<em>difficoltà nella digestione</em> dei grassi, proteine, amidi
	</li>
	<li>
		<em>carenza di vitamine</em> liposolubili
	</li>
	<li>
		perdita progressiva della <em>funzione polmonare</em>
	</li>
</ul>
<p>
	La malattia non danneggia in alcun modo le capacità intellettive e non si manifesta
	sull’aspetto fisico né alla nascita né in seguito nel corso della vita, per questo viene definita
	la “malattia invisibile”.
</p>
<div class="box">
	<h3 id="diffusione">
		Diffusione della malattia
	</h3>
	<p>
		Si stima che ogni 2.500-3.000 dei bambini nati in Italia, 1 è affetto da fibrosi cistica
		(circa 200 nuovi casi all’anno).
		<br/>
		La malattia colpisce indifferentemente maschi e femmine.
		Oggi quasi 6.000 bambini, adolescenti e adulti affetti da FC vengono curati nei <a href="https://www.fibrosicistica.it/lifc/centri-cura-associazioni-regionali/">Centri
		Specializzati in Italia</a>. Per merito dei continui progressi terapeutici, assistenziali,
		nonché diagnostici, la prevalenza dei pazienti FC in Italia, con età uguale e superiore
		ai 18 anni, è del 60.5% (Report at-a-glance RIFC anno 2020)
		<!-- TODO: Linkare al Report RIFC -->
	</p>
</div>
<div class="box">
	<h3 id="cause">
		Cause della Fibrosi Cistica
	</h3>
	<p>
		La malattia si manifesta quando un bambino eredita due copie alterate, cioè mutate, del <em>gene
		CFTR</em>, una da ciascun genitore. Il gene CFTR codifica la sintesi della proteina CFTR, che se
		ben funzionante, regola il movimento del cloro, al quale segue il movimento dell’acqua,
		dall’interno verso l’esterno delle cellule epiteliali delle ghiandole mucose.
		<br/>
		I genitori che hanno solamente una copia alterata del gene CFTR non hanno la fibrosi cistica,
		né evidenziano alcun sintomo della malattia e sono definiti <em>portatori sani del gene</em> della
		fibrosi cistica. Possono però trasmettere il gene difettoso ai figli, così come trasmettono
		altre caratteristiche, come il colore degli occhi e dei capelli.
		<br/>
		La frequenza dei portatori sani di mutazioni del gene FC in Italia e nel mondo occidentale
		è approssimativamente di <em>1 ogni 25/26 persone</em>. Quando due genitori portatori sani,
		cioè portatori entrambi di una mutazione, hanno un figlio, esiste <em>1 probabilità su 4</em>
		che il bambino nasca con FC.
	</p>
</div>
<div class="box">
	<h3 id="sintomi">
		I sintomi della fibrosi cistica
	</h3>
	<p>
		Il malfunzionamento o l’assenza della proteina CFTR interessa tutte le ghiandole a
		secrezione mucosa determinando una <em>carenza di cloro e acqua nelle secrezioni</em>. Le
		secrezioni mucose povere di acqua e vischiose tendono a ristagnare provocando così
		l’ostruzione degli organi interessati, prevalentemente bronchi, intestino e pancreas.
		Nell’albero bronchiale (il cui grado di coinvolgimento è il principale responsabile della
		gravità della malattia), si innesca un circolo vizioso infiammazione-infezione che porta
		infine alla destrutturazione del tessuto polmonare. La fibrosi cistica è una malattia che
		colpisce molti organi e che produce una varietà di sintomi tra cui:
	</p>
	<ul>
		<li>
			<em>Tosse persistente</em> dapprima stizzosa poi catarrale
		</li>
		<li>
			<em>Respiro sibilante e affanno</em>
		</li>
		<li>
			<em>Infezioni bronchiali e polmonari</em> frequenti
		</li>
		<li>
			<em>Diarrea cronica</em> con emissione feci oleose e maleodoranti, in alcuni casi
			<em>ostruzione intestinale</em> alla nascita (ileo da meconio) e spesso ostruzioni intestinali ripetute in età adolescenziale e adulta
		</li>
		<li>
			<em>Scarso accrescimento</em> in peso e altezza
		</li>
		<li>
			<em>Sudore salato</em>
		</li>
	</ul>
</div>
<div class="box">
	<h3 id="cura">
		Esiste una cura per la fibrosi cistica?
	</h3>
	<p>
		<em>La terapia</em> di questa malattia ha avuto negli ultimi anni un <em>notevole sviluppo</em>.
		Infatti, accanto ad una terapia dei sintomi adesso si comincia a disporre di <em>terapie
		personalizzate</em> che curano il <em>difetto di base</em> in alcune forme geniche di FC e si spera che,
		entro alcuni anni, tutte le mutazioni genetiche saranno curabili.
	</p>
</div>
